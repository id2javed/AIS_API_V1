package com.app.main.model;

public class CertAuthListResponse {
	private String certauthId, certauthAgency, certauthCode;

	public String getCertauthId() {
		return certauthId;
	}

	public void setCertauthId(String certauthId) {
		this.certauthId = certauthId;
	}

	public String getCertauthAgency() {
		return certauthAgency;
	}

	public void setCertauthAgency(String certauthAgency) {
		this.certauthAgency = certauthAgency;
	}

	public String getCertauthCode() {
		return certauthCode;
	}

	public void setCertauthCode(String certauthCode) {
		this.certauthCode = certauthCode;
	}
	
	
}
